    <h1 class="display-4"><strong>Aparat Desa Dilatih Pengembangan Usaha</strong></h1>
        <p>
        BANDA ACEH – Sebanyak 150 aparat desa mengikuti bimbingan teknis (Bimtek) bidang pengembangan usaha ekonomi desa di Hotel Grand Nanggroe, Banda Aceh, 31 Oktober-2 November 2015. Acara ini diadakan Badan Pemberdayaan Masyarakat (BPM) Aceh.
        </p><p>
        Bimtek tersebut dibuka oleh Dirjen Pembangunan dan Pemberdayaan Masyarakat Desa (PPMD) Kementerian Desa, Pembangunan Daerah Tertinggal, dan Transmigrasi yang diwakili Direktur Pengembangan Usaha Ekonomi Desa (PUED), Drs H Sugeng Riyono MSi, Minggu (1/11) pagi.
        </p><p>
        Sugeng saat membacakan sambutan tertulis Dirjen PPMD antara lain mengatakan, tujuan dilaksanakan bimtek tersebut adalah sebagai tindak lanjut atas upaya pembinaan dan pendampingan sesuai tugas dan tanggung jawab yang tercantum dalam UU Desa. “Tujuan lain, untuk memfasilitasi peningkatan kapasitas aparat desa dan meningkatkan peran BPM provinsi dan kabupaten/kota dalam hal pembinaan, pelaksanaan, dan pendampingan pengembangan usaha ekonomi desa,” ungkapnya.
        </p><p>
        Dengan adanya bimtek ini, Sugeng berharap kapasitas aparat desa, fasilitator desa, kepala BPM provinsi dan kabupaten/kota, serta pejabat yang menangani bidang pengembangan usaha ekonomi desa dapat meningkat. Sehingga tekad pemerintah melalui Dirjen PPMD untuk mewujudkan Indonesia yang berdikari dalam bidang ekonomi akan mudah terwujud.
        </p><p>
        Sementara itu, Kepala BPM Aceh, Drs Zulkifli Hs MM menyebutkan, peserta bimtek tersebut berasal dari lima kabupaten/kota di Aceh yaitu Aceh Besar, Pidie, Pidie Jaya, dan Aceh Jaya. “Setiap kabupaten/kota diwakili oleh 10 gampong, dimana setiap gampong mengirim tiga peserta yaitu keuchik, tuha peut, dan pengelola badan usaha milik gampong (BUMG),” ujarnya.
        </p><p>
        Adapun narasumber yang diundang, lanjut Zulkifli, yaitu unsur Dirjen PPMD Kemendes, PDT, dan Transmigrasi, Pemerintah Aceh yang diwakili BPM Aceh serta Dinas Koperasi dan UKM, akademisi dari Fakultas Ekonomi Unsyiah, dan praktisi ekonomi. “Kami berharap peserta dapat mengikuti bimtek ini dengan baik. Sehingga ilmu yang diperoleh dapat diimplementasikan dalam tugas sehari-hari,” harap Zulkifli Hs.
        </p><p>
